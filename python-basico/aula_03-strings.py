"""
Python - Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('- Hello, World!')
print('- Hello, "World!"')

# Aspas duplas
print("- Hi, World!")
print("- Hi, 'World!'")

# Escape
print("- Hi, \"World!\"")

# r
print(r"- Hello,\"World\"")